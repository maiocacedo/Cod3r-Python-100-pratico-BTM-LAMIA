"""todos os tipos""" 


# Integer
inteiro = 1
print(type(inteiro))
print(inteiro, end="\n\n")


# Float
decimal = 1.1
print(type(decimal))
print(decimal, end="\n\n")


# String
texto = 'texto'
print(type(texto))
print(texto, end="\n\n")


# List
lista = [1, 2, 3, 4, 5]
print(type([1,2]))
print(lista, end="\n\n")


# Set
conj = {1, 2, 3, 4}
print(type(conj))
print(conj, end="\n\n")


# Boolean
aluno_passou = True
print(type(aluno_passou))
print(aluno_passou, end="\n\n")


# Tuples
nomes = ('Ana', 'Bia', 'Gui', 'Ana')
print(type(nomes))
print(nomes[1:3], end='\n\n')


# Dictionary
aluno = {
    'nome': 'Pedro Henrique',
    'Nota': 9.2,
    'ativo': True
}
print(type(aluno))
print(aluno, end="\n\n")
